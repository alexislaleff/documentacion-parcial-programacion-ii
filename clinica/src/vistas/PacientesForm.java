    void listar(List<Pacientes> lista) {
        modelo = (DefaultTableModel) tabla.getModel();
        Object[] ob = new Object[6];
        for (int i = 0; i < lista.size(); i++) {
            ob[0] = lista.get(i).getDni();
            ob[1] = lista.get(i).getNombre();
            ob[2] = lista.get(i).getApellido();
            ob[3] = lista.get(i).getFechaNacimiento();
            ob[4] = lista.get(i).getTelefono();
            ob[5] = lista.get(i).getSexo();
            modelo.addRow(ob);
        }
        tabla.setModel(modelo);
    }

    void filtrar() {
        if ("DNI".equals(txtFiltrar.getSelectedItem().toString())) {
            List<Pacientes> lista = con.listarDniFiltro(Integer.parseInt(txtBuscar.getText()));
            listar(lista);
        } else if ("Nombre".equals(txtFiltrar.getSelectedItem().toString())) {
            List<Pacientes> lista = con.listarNombre(txtBuscar.getText());
            listar(lista);
        } else {
            List<Pacientes> lista = con.listarNombre(txtBuscar.getText());
            listar(lista);
        }
    }

    boolean esNumerico(String cadena) {
        try {
            Integer.parseInt(cadena);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }

