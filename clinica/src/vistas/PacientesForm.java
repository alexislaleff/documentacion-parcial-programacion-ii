    void agregar() {
        if (esNumerico(txtDni.getText()) && esNumerico(txtTel.getText())) {
            String dni = txtDni.getText();
            String nombre = txtNombre.getText();
            String apellido = txtApellido.getText();
            Date fechaNac = txtFechaNac.getDate();
            String tel = txtTel.getText();
            String sexo = txtSexo.getText();

            Object[] ob = new Object[6];
            ob[0] = dni;
            ob[1] = nombre;
            ob[2] = apellido;
            ob[3] = fechaNac;
            ob[4] = tel;
            ob[5] = sexo;
            con.agregar(ob);
            limpiarTabla();
            listar(con.Listar());
            limpiar();
        } else {
            JOptionPane.showMessageDialog(this, "Algunos campos solo reciben datos numericos");
        }
    }

    void actualizar() {
        if (esNumerico(txtDni.getText()) && esNumerico(txtTel.getText())) {
            int fila = tabla.getSelectedRow();
            if (fila == -1) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
            } else {
                String dni = txtDni.getText();
                String nombre = txtNombre.getText();
                String apellido = txtApellido.getText();
                Date fechaNac = txtFechaNac.getDate();
                String tel = txtTel.getText();
                String sexo = txtSexo.getText();

                try {
                    Object[] ob = new Object[6];
                    ob[0] = dni;
                    ob[1] = nombre;
                    ob[2] = apellido;
                    ob[3] = fechaNac;
                    ob[4] = tel;
                    ob[5] = sexo;
                    con.actualizar(ob);
                    limpiarTabla();
                    listar(con.Listar());
                    limpiar();
                } catch (Exception e) {
                    System.out.println(e);
                }
                ;
            }
        } else {
            JOptionPane.showMessageDialog(this, "Algunos campos solo reciben datos numericos");
        }
    }

    void eliminar() {
        int fila = tabla.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        } else {
            int dni = Integer.parseInt(tabla.getValueAt(fila, 0).toString());
            con.eliminar(dni);
        }
    }

    void limpiar() {
        txtDni.setText("");
        txtNombre.setText("");
        txtApellido.setText("");
        txtTel.setText("");
        txtSexo.setText("");
        txtDni.requestFocus();
    }

    void limpiarTabla() {
        modelo.getDataVector().removeAllElements();
        modelo.fireTableDataChanged();
    }

